package model;

import java.io.Serializable;

public class Point implements Serializable {
    private int id, x, y;
    private Pair pair;

    public Point() {
    }

    public int getId() {
        return id;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public Pair getPair() {
        return pair;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setPair(Pair pair) {
        this.pair = pair;
    }
}
